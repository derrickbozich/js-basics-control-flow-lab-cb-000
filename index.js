function scuberGreetingForFeet(value){
  let charge;
  if (value <= 400) {
    charge = "This one is on me!";
  } else if (value > 2000) {
    charge = "I will gladly take your thirty bucks.";
  } else if (value > 2500)  {
    charge = 'No can do.';
  }
  charge;
}
